package com.elstele.bill.filesWorkers;

public class DOCXTransTemplate {
    private String directionName;
    private String prefMainPart;
    private String prefEnder;
    private String tariff;

    public String getDirectionName() {
        return directionName;
    }

    public void setDirectionName(String directionName) {
        this.directionName = directionName;
    }

    public String getPrefMainPart() {
        return prefMainPart;
    }

    public void setPrefMainPart(String prefMainPart) {
        this.prefMainPart = prefMainPart;
    }

    public String getPrefEnder() {
        return prefEnder;
    }

    public void setPrefEnder(String prefEnder) {
        this.prefEnder = prefEnder;
    }

    public String getTariff() {
        return tariff;
    }

    public void setTariff(String tariff) {
        this.tariff = tariff;
    }
}
